function makePortfolio(
  seed: number,
  categories: string[]
): PortfolioItem[] {
  const start = (seed * 3) % 34;
  return Array.from({ length: 6 }).map((_, index) => {
    const imageNumber = String(start + index + 1).padStart(2, "0");
    return {
      id: `p-${seed}-${index + 1}`,
      image: `/images/portfolio/style-${imageNumber}.png`,
      category: categories[index % categories.length],
      title: `${categories[index % categories.length]} 스타일`,
    };
  });
}

